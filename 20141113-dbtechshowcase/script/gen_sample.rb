require('json')

def json_fp
  @json_fp ||= File::open("sample_#{N}.js", 'w')
end

def sql_fp
  @sql_fp ||= File::open("sample_#{N}.sql", 'w')
end

def sample(person)
  @_id ||= 1
  person[:_id] = @_id;
  @_id += 1
  person
end

def out_json(people)
  people.each{ |person|
    json_fp.puts person.to_json
  }
end

def out_sql(people)
  values = people.map{ |person|
    "(#{person[:_id]}, \"#{person[:first]}\", \"#{person[:family]}\", \"#{person[:sex]}\")"
  }.join(',')
  sql_fp.puts "INSERT INTO people VALUES #{values};"

  values = people.map{ |person|
    if person[:hobies].size > 0
      person[:hobies].map{|hoby| "(#{person[:_id]}, \"#{hoby}\")" }.join(',')
    end
  }.compact.join(',')
  sql_fp.puts "INSERT INTO hobies VALUES #{values};" unless values == ""
end

BATCH_SIZE = 200;

def gen_samples
  samples = []
  N.times.each {
    samples << sample(gen_male)
    samples << sample(gen_female)
    if samples.size >= BATCH_SIZE
      out_json(samples)
      out_sql(samples)
      samples = []
    end
  }
  if samples.size > 0
    out_json(samples)
    out_sql(samples)
  end
end

gen_samples

json_fp.close
sql_fp.close
